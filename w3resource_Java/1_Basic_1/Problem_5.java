//Write a Java program to print the sum (addition), multiply, subtract, divide
// and remainder of two numbers.
public class Problem_5{
    public  static void main(String[] args){
        int a = 150;
        int b=56;
        //sum(addition)
        int sum = a+b;
        System.out.println("Sum of a and b:"+sum);
        //multiply
        int multiply = a *b;
        System.out.println("Multiplication of a and b:"+multiply);
        //subtraction
        int subtraction = a-b;
        System.out.println("subtraction of a and b:"+subtraction);
        //division
        int division = a /b;
        System.out.println("Division of a and b:"+division);
    }
}